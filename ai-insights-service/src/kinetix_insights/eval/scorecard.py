"""Render the offline eval scorecard as a Markdown governance artefact.

Run as a module::

    uv run python -m kinetix_insights.eval.scorecard --out ../docs/governance/copilot-eval-scorecard.md

The output is framed for a model-governance reviewer (SR 11-7 / BCBS
239): it states what the AI copilot is, which controls are tested, and
the measured catch-rates, with a per-case table so a reviewer can see
exactly which adversarial inputs were exercised. It is regenerable —
re-run after changing the dataset or the guards and commit the diff.

The numbers come straight from the production guards via
:func:`kinetix_insights.eval.runner.run_offline`; this module only
formats them.
"""

from __future__ import annotations

import argparse
from pathlib import Path

from kinetix_insights.eval.dataset import DEFAULT_DATASET_PATH, load_cases
from kinetix_insights.eval.models import Scorecard
from kinetix_insights.eval.runner import run_offline

_VERDICT_DETAIL = {
    "ok": "delivered",
    "policy_blocked": "blocked — advisory language",
    "citation_blocked": "blocked — uncited figure/ticker",
}


def _pct(value: float) -> str:
    return f"{value * 100:.1f}%"


def render_markdown(scorecard: Scorecard) -> str:
    """Build the full Markdown report from a scored dataset."""

    lines: list[str] = []
    a = lines.append

    a("# Copilot Eval Scorecard")
    a("")
    a(
        "> Regenerable model-governance artefact for the Kinetix AI copilot. "
        "Generated by `python -m kinetix_insights.eval.scorecard`; do not hand-edit."
    )
    a("")
    a("## What this measures")
    a("")
    a(
        "The AI copilot (see [ADR-0036](../adr/0036-ai-copilot-architecture.md) "
        "and [`specs/ai-insights.allium`](../../specs/ai-insights.allium)) "
        "answers risk questions over read-only MCP tools. Three runtime controls "
        "constrain its output:"
    )
    a("")
    a(
        "- **Citation verification** — every numeric token and ticker in a "
        "delivered answer must trace to a tool result, or the turn is blocked "
        "(`CITATION_UNVERIFIABLE`)."
    )
    a(
        "- **Policy guard** — advisory / recommendation language is blocked "
        "(`POLICY_VIOLATION`); the copilot informs, it does not advise."
    )
    a(
        "- **Prompt-injection sanitiser** — known injection patterns in the "
        "prompt and embedded page context are neutralised before the model runs."
    )
    a("")
    a(
        "This scorecard replays a curated golden dataset — including deliberately "
        "adversarial inputs — through those same production guards and reports "
        "whether each control behaved as specified. It is the offline, "
        "deterministic gate; a live LLM-as-judge run (`pytest -m eval_live`) "
        "complements it against the real model."
    )
    a("")

    a("## Headline metrics")
    a("")
    a("| Metric | Result |")
    a("| --- | --- |")
    a(f"| Cases evaluated | {scorecard.total} |")
    a(f"| Overall pass rate | {_pct(scorecard.pass_rate)} |")
    a(
        "| Citation guard catch-rate (recall) | "
        f"{_pct(scorecard.citation_catch_rate)} |"
    )
    a(f"| Policy guard catch-rate (recall) | {_pct(scorecard.policy_catch_rate)} |")
    a(
        "| Prompt-injection neutralisation | "
        f"{_pct(scorecard.injection_catch_rate)} |"
    )
    a(
        "| Clean-answer pass-rate (no false positives) | "
        f"{_pct(scorecard.clean_pass_rate)} |"
    )
    a("")

    a("## By category")
    a("")
    a("| Category | Passed | Total | Pass rate |")
    a("| --- | --- | --- | --- |")
    for cat in scorecard.category_scores():
        a(f"| {cat.category} | {cat.passed} | {cat.total} | {_pct(cat.pass_rate)} |")
    a("")

    a("## Per-case results")
    a("")
    a("| Case | Category | Expected | Actual | Pass |")
    a("| --- | --- | --- | --- | --- |")
    for r in scorecard.results:
        expected = _VERDICT_DETAIL.get(
            r.expected_verdict.value, r.expected_verdict.value
        )
        actual = _VERDICT_DETAIL.get(r.actual_verdict.value, r.actual_verdict.value)
        mark = "✅" if r.passed else "❌"
        a(f"| `{r.case_id}` | {r.category} | {expected} | {actual} | {mark} |")
    a("")

    return "\n".join(lines)


def main(argv: list[str] | None = None) -> int:
    parser = argparse.ArgumentParser(description="Render the copilot eval scorecard.")
    parser.add_argument(
        "--out",
        type=Path,
        required=True,
        help="Path to write the Markdown scorecard to.",
    )
    parser.add_argument(
        "--dataset",
        type=Path,
        default=DEFAULT_DATASET_PATH,
        help="Override the golden dataset path.",
    )
    args = parser.parse_args(argv)

    scorecard = run_offline(load_cases(args.dataset))
    markdown = render_markdown(scorecard)

    args.out.parent.mkdir(parents=True, exist_ok=True)
    args.out.write_text(markdown)
    print(
        f"wrote {args.out} — {scorecard.total} cases, "
        f"pass rate {_pct(scorecard.pass_rate)}"
    )
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
